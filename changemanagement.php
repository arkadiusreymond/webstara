              <p style="text-align:justify;text-justify:inter-word;">
			  Perubahan sistem merupakan hal yang biasa terjadi di dalam organisasi. Dari sisi teknologi informasi perubahan meliputi perangkat keras, perangkat lunak baik sistem operasi maupun aplikasi, pada sistem manajemen dan pendukung database, pada sistem jaringan. Sedangkan dari sisi non teknis meliputi prosedur dan tenaga manusia pelaksana.
			  Metode konversi ke sistem baru meliputi perpindahan langsung (direct cutover), piloting oleh sebagian dari komponen perusahaan yang dipilih, bertahap dalam sistem baru (modular) dan cara paralel yaitu sistem baru dimulai tapi sistem lama sementara masih dipertahankan dan dihentikan sesudah sistem baru dipastikan berjalan normal.
			  Hal yang terpenting dalam Change Management adalah meminimalkan resiko yang bisa terjadi, berupa antisipasi supaya kegiatan bisnis tetap berjalan. Semua upaya ini dimasukkan dalam satu dokumen yang disebut Business Continuity Plan (BCP) atau Rencana Kelangsungan Bisnis.
			  Karena faktor manusia memiliki peran penting dalam proses konversi, maka persiapan harus dilakukan secukupnya meliputi kesadaran (awareness) mengapa terjadi perubahan sistem, hasrat (desire) untuk mendukung dan ambil bagian dalam perubahan ini, pengetahuan (knowledge) tentang bagaimana perubahan itu terjadi,
			  kemampuan (ability) untuk menggunakan ketrampilan dan perilaku yang baru, dan penguatan (reinforcement) untuk mempertahankan perubahan.<br/><br/>
<img src="images/cm.jpg"  alt="sop" /><br/><br/>
Komponen yang harus ada di dalam Change Management adalah : <br/><br/>
<strong>1.       Motivating Change<br/></strong>
Mendorong kesiapan untuk berubah dan mengatasi setiap penolakan terhadapnya. <br/>
<strong>2.       Creating a Vision<br/></strong>
Merumuskan arah perubahan yang diharapkan. <br/>
<strong>3.       Developing Political Support<br/></strong>
Mempersiapkan para Agen Perubah (Change Agent), termasuk para informal leader. <br/>
<strong>4.       Managing the Transition<br/></strong>
Menyusun rencana aktivitas, membangun komitmen dan struktur komite. <br/>
<strong>5.       Sustaining Momentum<br/></strong>
Mempersiapkan infrastruktur perubahan, membangun sistem pendukung bagi para Agen Perubah, membangun kompetensi dan keahlian baru, dan mengapresiasi kemajuan sekecil apapun. <br/><br/>

Bila salah satu komponen di atas tidak dilaksanakan dengan baik, maka beresiko terhadap keberhasilan perubahan itu sendiri, seperti: <br/><br/>
Langkah 1 tidak dijalankan / cukup seadanya = Status Quo akan terjaga di titik ekstrim. <br/>
Langkah 2 tidak dijalankan / cukup seadanya = Perubahan terjadi dengan arah yang tidak jelas dan terjadi kebingungan. <br/>
Langkah 3 tidak dijalankan / cukup seadanya = Akan terjadi sabotase. <br/>
Langkah 4 tidak dijalankan / cukup seadanya = Fungsi-fungsi di dalam organisasi akan sulit beroperasi. <br/>
Langkah 5 tidak dijalankan / cukup seadanya = Perubahan tidak akan mencapai titik yang diharapkan. <br/><br/>

Berdasarkan resiko yang telah diuraikan diatas STARA sebagai penyedia layanan change manajemen, siap membantu para klien untuk melakukan perubahan apapun, kebijakan, lokasi, waktu kerja, dst. Dengan perencanaan yang matang dan dilaksanakan serta dimonitor dengan baik dan hati-hati.

			  
